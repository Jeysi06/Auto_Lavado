public void cargarVehiculo() {
        modeloTabla.setRowCount(0);
        List<vehiculoModel> vehiculo = vehiculoController.obtenerTodos();
        for (vehiculoModel r : vehiculo) {
            modeloTabla.addRow(new Object[]{r.getIdVehiculo(), r.getMatricula(), r.getMarca(), r.getModelo(), r.getColor(), r.getAnio(), r.getTipo(), r.getIdCliente()});
        }
    }

    public void agregarVehiculo() {
        String Matricula = txtMatricula.getText();
        String Marca = txtMarca.getText();
        String Modelo = txtModelo.getText();
        String Color = txtColor.getText();
        String Anio = txtAnio.getText();
        String Tipo = "Carro";
        int IdCliente = 1;
        if (Matricula.isEmpty() || Marca.isEmpty() || Modelo.isEmpty() || Color.isEmpty() || Anio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "CamposObligatorios");
        } else {
            vehiculoController.insertarVehiculo(new vehiculoModel(Matricula, Marca, Modelo, Color, Anio, Tipo, IdCliente));
            txtMatricula.setText(" ");
            txtMarca.setText(" ");
            txtModelo.setText(" ");
            txtColor.setText(" ");
            txtAnio.setText(" ");

            cargarVehiculo();
        }
    }

    public void buscarVehiculo() {
        String vehiculo = JOptionPane.showInputDialog(this, "Introduce la matricula");
        modeloTabla.setRowCount(0);
        List<vehiculoModel> vehiculos = vehiculoController.buscarVehiculoNombre(vehiculo);
        for (vehiculoModel r : vehiculos) {
            modeloTabla.addRow(new Object[]{r.getIdVehiculo(), r.getMatricula(), r.getMarca(), r.getModelo(), r.getColor(), r.getAnio(), r.getTipo(), r.getIdCliente()});

        }
    }

    public void seleccionaVehiculo() {
        tblVehiculo.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int fila = tblVehiculo.getSelectedRow();
                if (fila != -1) {
                    idVehiculo = Integer.parseInt(tblVehiculo.getValueAt(fila, 0).toString());
                    tipoObtenido = 1;
                    clienteObtenido = 1;
                    txtMatricula.setText(tblVehiculo.getValueAt(fila, 1).toString());
                    txtMarca.setText(tblVehiculo.getValueAt(fila, 2).toString());
                    txtModelo.setText(tblVehiculo.getValueAt(fila, 3).toString());
                    txtColor.setText(tblVehiculo.getValueAt(fila, 4).toString());
                    txtAnio.setText(tblVehiculo.getValueAt(fila, 5).toString());
                } else {
                    JOptionPane.showConfirmDialog(this, "NO hay vehiculos resgitrados");
                }
            }
        });
    }
     private void eliminarVehiculos(){
        int fila=tblVehiculo.getSelectedRow();
        if(fila!=-1){
            int id=Integer.parseInt(tblVehiculo.getValueAt(fila,0).toString());
            vehiculoController.eliminarVehiculo(id);
            cargarVehiculo();
        }else{
            JOptionPane.showMessageDialog(this, "Selecciona el vehiculo a eliminar");
        }
    }
     public void modificarVehiculo() {
        String Matricula = txtMatricula.getText();
        String Marca = txtMarca.getText();
        String Modelo = txtModelo.getText();
        String Color = txtColor.getText();
        String Anio = txtAnio.getText();
        String Tipo = "Carro";
        int IdCliente = 1;
        if (Matricula.isEmpty() || Marca.isEmpty() || Modelo.isEmpty() || Color.isEmpty() || Anio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "CamposObligatorios");
        } else {
            vehiculoController.actualizarVehiculo(new vehiculoModel(this.idVehiculo,Matricula, Marca, Modelo, Color, Anio, Tipo, IdCliente));
            txtMatricula.setText(" ");
            txtMarca.setText(" ");
            txtModelo.setText(" ");
            txtColor.setText(" ");
            txtAnio.setText(" ");

            cargarVehiculo();
        }
    }
